function callClaude(string $apiKey, array $messages, string $system, int $maxTokens = 1024): string {
    $payload = json_encode([
        'model' => 'claude-sonnet-4-6',
        'max_tokens' => $maxTokens,
        'system' => $system,
        'messages' => $messages,
    ]);

    $ch = curl_init('https://api.anthropic.com/v1/messages');
    curl_setopt_array($ch, [
        CURLOPT_POST => true,
        CURLOPT_POSTFIELDS => $payload,
        CURLOPT_RETURNTRANSFER => true,
        CURLOPT_HTTPHEADER => [
            'Content-Type: application/json',
            'x-api-key: ' . $apiKey,
            'anthropic-version: 2023-06-01',
        ],
    ]);

    $response = curl_exec($ch);
    if ($response === false) {
        throw new Exception('Claude API への接続に失敗しました: ' . curl_error($ch));
    }

    $data = json_decode($response, true);
    if (isset($data['error'])) {
        throw new Exception('Claude API エラー: ' . $data['error']['message']);
    }
    return $data['content'][0]['text'] ?? 'エラーが発生しました';
}
